    private void EnSonKayitNosunuCek() {
        initComponents();
        txt_belgeNo.setEnabled(false);

        try {
            VeritabaniBaglanti baglanti = new VeritabaniBaglanti();
            ResultSet res = baglanti.GarantiSonKayit();
            while (res.next()) {
                txt_belgeNo.setText(String.valueOf(res.getInt("GarantiBelge_KayitNo") + 1));//en son kayitnosunu bulup bir artiriyor
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Son Kayit Getirilemedi Lütfen Yöneticinize Başvurun.");

        }
    }

