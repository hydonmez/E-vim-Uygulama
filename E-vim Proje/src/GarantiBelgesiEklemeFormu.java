    private void BtnGarantiBelgesiEklemeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGarantiBelgesiEklemeActionPerformed

        try {
            if (txt_belgeNo.getText().isEmpty() || txt_fatura.getText().isEmpty() || txt_marka.getText().isEmpty() || txt_model.getText().isEmpty() || txt_seriNo.getText().isEmpty()
                    || txt_urunAdi.getText().isEmpty() || dc_garantiBitisTarihi.getDate() == null) {
                JOptionPane.showMessageDialog(null, "Hiç Bir Alan Boş Geçilemez!");
            } else {
                garantiBelgesi.setGarantiBelge_KayitNo(Integer.parseInt(txt_belgeNo.getText()));
                garantiBelgesi.setGarantiBelge_Markasi(txt_marka.getText());
                garantiBelgesi.setGarantiBelge_Turu(cmbx_urunTuru.getSelectedItem().toString());
                garantiBelgesi.setGarantiBelge_Modeli(txt_model.getText());
                garantiBelgesi.setGarantiBelge_Adi(txt_urunAdi.getText());
                garantiBelgesi.setGarantiBelge_SeriNo(txt_seriNo.getText());
                garantiBelgesi.setGarantiBelge_Faturasi(txt_fatura.getText().replace("\\", "/"));
                garantiBelgesi.setGarantiBelge_GarantiBitisTarihi(TarihDonustur(dc_garantiBitisTarihi.getDate()));
                garantiBelgesiIslemleri.GarantiBelgesiEkle(garantiBelgesi);
                JOptionPane.showMessageDialog(null, " Kaydınız Başarıyla Tamamlanmıştır! ");
                Temizle();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Garanti Belgesi Ekleme İşlemi Gerçekleştirilemedi! ");
        }

    }//GEN-LAST:event_BtnGarantiBelgesiEklemeActionPerformed

    private void txt_faturaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txt_faturaMouseClicked
 //fatura dekontunun yolu alinir
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("JPG & GIF Images", "png", "gif");//Fitreliyoruz
        fileChooser.setFileFilter(filter);
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.dir")));//Gecerli projenin dosya yolu
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) 
        {
            File selectedFile = fileChooser.getSelectedFile();
            try 
            {
                txt_fatura.setText(selectedFile.getCanonicalPath());
            } 
            catch (IOException ex) 
            {
                     JOptionPane.showMessageDialog(null, "Garanti Belgesi Dekont Seçme İşlemi Gerçekleştirilemedi! "+ex);

                // JOptionPane.showMessageDialog(null, "Dekont Seçme  İşlemi Gerçekleştirilemedi! ");
               // Logger.getLogger(FaturaEklemeFormu.class.getName()).log(Level.SEVERE, null, ex);// loglama islemininde ornegi olsun diye loglama yapildi
            }
        }
    }//GEN-LAST:event_txt_faturaMouseClicked

