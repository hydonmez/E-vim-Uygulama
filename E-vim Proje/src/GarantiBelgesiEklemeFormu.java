    private String TarihDonustur(Date tarih) {
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");//tarih formatını veritabanına eklemek için gerekli kodlar
        String faturaTarihi = format.format(tarih);
        return faturaTarihi;
    }

