    private void BtnFaturaGosterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnFaturaGosterActionPerformed

      try {
            //acilacak dosyanın yolu verilir 
            File file = new File(txt_faturaDekont.getText());
            if (!Desktop.isDesktopSupported()) // destkleniyor mu desteklenmiyor mu kontrolu yapilir
            {
                System.out.println("Dosya Turu Deklenemiyor");
                return;
            }
            Desktop desktop = Desktop.getDesktop();
            if (file.exists()) //dosyanin olup olmadigi kontrol edilir
            {
                desktop.open(file); //dosya varsa acilir  
            } else//Dosya yolu yoksa bulunamadi uyarisi verilir
            {
                JOptionPane.showMessageDialog(null, "Dosya Yolu Bulunamadi! ");
            }
        } catch (IOException e) {
            e.printStackTrace();//Bu yontem, aynı e nesnesinin mesajini ve ayrica istisnanin olustugu satir numarasini yazdirir.
        }
    }//GEN-LAST:event_BtnFaturaGosterActionPerformed

    private void btnFaturaListeleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFaturaListeleActionPerformed

        try {
            ResultSet sonuc = faturaIslemleri.FaturaListele(Integer.parseInt(txt_faturaNo.getText()));//resultset geri doner.
            if (!sonuc.isBeforeFirst())//sonucun boş olup olmadığını kontrol eder
            {
                JOptionPane.showMessageDialog(null, "İstediğiniz kayıt numarasında fatura bulunamamiştir !");
            } else {
                while (sonuc.next()) {
                    txt_faturaNo.setText(sonuc.getString("Fatura_KayitNo"));
                    txt_faturaMiktari.setText(sonuc.getString("Fatura_Miktari"));
                    txt_faturaDekont.setText(sonuc.getString("Fatura_Dekont"));
                    cmbx_faturaTuru.setSelectedItem(sonuc.getString("Fatura_Turu"));

                    String faturaTarihi = sonuc.getString("Fatura_Tarihi");
                    Date date = new SimpleDateFormat("yyyy-MM-dd").parse(faturaTarihi);//++ yyyy-mm-dd formatına cevirerek date nesnesine atılır++
                    dc_faturaTarihi.setDate(date);//++olusan date nesnesi date choosere verilir ve fatura tarihi gosterilir

                    String odemeTarihi = sonuc.getString("Fatura_OdemeTarihi");//tarihi jchoosera çekmek için ilk bir stringe çekiyoruz sonra alttarafta pars edip ekliyoruz++
                    Date date1 = new SimpleDateFormat("yyyy-MM-dd").parse(odemeTarihi);
                    dc_faturaOdemeTarihi.setDate(date1);

                    JOptionPane.showMessageDialog(null, "Kaydınız Başarıyla Listelenmistir!");
                    txt_faturaNo.setEnabled(false);  ///Gerekli ayarlamalar yapilir
                    BtnFaturaGuncelle.setEnabled(true);
                    BtnFaturaSil.setEnabled(true);
                    BtnFaturaGoster.setEnabled(true);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Lütfen Kayıt Numarası Giriniz! ");
        }
    }//GEN-LAST:event_btnFaturaListeleActionPerformed

