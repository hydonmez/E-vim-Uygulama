    private void BtnFaturaGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnFaturaGuncelleActionPerformed
        try {
            fatura.setFatura_KayitNo(Integer.parseInt(txt_faturaNo.getText()));
            fatura.setFatura_Turu(cmbx_faturaTuru.getSelectedItem().toString());
            fatura.setFatura_Miktari(Integer.parseInt(txt_faturaMiktari.getText()));
            fatura.setFatura_Dekont((txt_faturaDekont.getText()).replace("\\", "/"));
            fatura.setFatura_OdemeTarihi(TarihDonustur(dc_faturaOdemeTarihi.getDate()));
            fatura.setFatura_Tarihi(TarihDonustur(dc_faturaTarihi.getDate()));
            faturaIslemleri.FaturaGuncelle(fatura);//guncellenecek olan fatura fatura islemlerindeki fatura guncelleye gonderilir ve guncellenir

            TablolariGetir();//guncellenme sonrasında datagrid en son tablo cekilir
            Temizle();//formda doldurulan alanlar temizlenir
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Fatura Güncellenemedi! ");
        }
    }//GEN-LAST:event_BtnFaturaGuncelleActionPerformed

