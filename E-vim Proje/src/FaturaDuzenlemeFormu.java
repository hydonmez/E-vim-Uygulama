    private void TablolariGetir() {
        FaturaModel.setRowCount(0);
        try {
            VeritabaniBaglanti baglanti = new VeritabaniBaglanti();//veritabaninda sinifindan bir nesne olusturuldu
            ResultSet sonEklenenFaturaSonuc = baglanti.SonEklenenFaturalar();//Sinifta bulunan SonEklenenFaturalar fonksiyonu cagrildi ve degiskene atandi
            FaturaModel.setColumnIdentifiers(faturaSutun);//yukarda tanımladıgimiz sutunlari columa ekledik icine attik
            while (sonEklenenFaturaSonuc.next()) //Son eklenen faturaya kadar döngüde next fonksiyonunu kullanarak döner 
            {
                faturaSatir[0] = sonEklenenFaturaSonuc.getString("Fatura_KayitNo");
                faturaSatir[1] = sonEklenenFaturaSonuc.getString("Fatura_Turu");
                faturaSatir[2] = sonEklenenFaturaSonuc.getString("Fatura_Tarihi");
                faturaSatir[3] = sonEklenenFaturaSonuc.getString("Fatura_OdemeTarihi");
                faturaSatir[4] = sonEklenenFaturaSonuc.getString("Fatura_Miktari");
                faturaSatir[5] = sonEklenenFaturaSonuc.getString("Fatura_Dekont");
                FaturaModel.addRow(faturaSatir);//yukarida taımladığımız satirları rowsa ekledik 		
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Tablo Getirilemedi! ");
        }
    }

